    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        StringBuilder lowStockMessage = new StringBuilder();
        try{
           Connection con = ConnectionPdr.getCon();
           Statement st = con.createStatement();
           ResultSet rs = st.executeQuery("select *from addmedicine");
           while(rs.next()){
               String medicineName = rs.getString("name");
               int quantity = rs.getInt("quantity");

            model.addRow(new Object[]{
                rs.getString("medicine_pk"),
                rs.getString("uniqueID"),
                rs.getString("name"),
                rs.getString("companyName"),
                rs.getString("quantity"),               
                rs.getString("price")
            });
            if (quantity < 20) {
                lowStockMessage.append(medicineName).append(" has less than 20 units remaining!\n");
            }
           // model.addRow(new Object[]{rs.getString("medicine_pk"),rs.getString("uniqueID"),rs.getString("name"),rs.getString("companyName"),rs.getString("quantity"),rs.getString("price")});
        } 
           if (lowStockMessage.length() > 0) {
            JOptionPane.showMessageDialog(
                this, 
                lowStockMessage.toString(), 
                "Low Stock Warning", 
                JOptionPane.WARNING_MESSAGE
            );
        }
           }
           catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_formComponentShown

