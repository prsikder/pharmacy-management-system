    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String id = model.getValueAt(index, 0).toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to delete this Medicine","Select",JOptionPane.YES_NO_OPTION);
        if(a==0){
            try{
               Connection con = ConnectionPdr.getCon();
               PreparedStatement ps = con.prepareStatement("delete from addmedicine where medicine_pk=?");
               ps.setString(1, id);
               ps.executeUpdate();
               JOptionPane.showMessageDialog(null,"Medicine deleted successfully!!");
               setVisible(false);
               new ViewMed().setVisible(true);
            }
            catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String medicineName = jTextField1.getText().trim();
    if (medicineName.equals("")) {
        JOptionPane.showMessageDialog(null, "Please enter a medicine name.");
        return;
    }

    try {
        Connection con = ConnectionPdr.getCon();
        PreparedStatement ps = con.prepareStatement("SELECT quantity, price FROM addmedicine WHERE name = ?");
        ps.setString(1, medicineName);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            int quantity = rs.getInt("quantity");
            int price = rs.getInt("price");

            jTextField2.setText(String.valueOf(quantity));
            jTextField3.setText(String.valueOf(price));
        } else {
            JOptionPane.showMessageDialog(null, "Medicine not found!");
            jTextField2.setText("");
            jTextField3.setText("");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

